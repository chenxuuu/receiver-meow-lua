--随机数表代码来自http://www.cnblogs.com/slysky/p/5954053.html
--产生1~~m,若有n的则m~~n的数字表
function table.fillNum(m,n)
    local j,k
    if n then
       j=m
       k=n
    else
        j=1
        k=m
    end
    local t={}
    for i=j,k do
        table.insert(t,i)
    end
    return t
end
--产生不相同的从m到n，一共cnt个随机数表
function math.randomx(m,n,cnt,seed)
    local tmp=table.fillNum(m,n)
    math.randomseed(seed)
    local t={}
    local x
    while cnt>0 do
        x = math.random(1,n-m+1)
        table.insert(t,tmp[x])
        table.remove(tmp,x)
        cnt=cnt-1
        m=m+1
    end
    return t
end

--获取运势结果
function getAlmanac(time,fromqq)
    local day = math.floor(time/(3600*24))--取整，唯一的某一天
    local seed = fromqq..tostring(day)--今日唯一的随机数种子
    local ran = math.randomx(1,#goodthings,6,seed)
    local saysTemp = says[math.random(1,#says)]
    --恢复随机数种子为随机值
    math.randomseed(tostring(os.time()):reverse():sub(1, 6))
    return("你的运势如下~\r\n"..
    "💡宜：\r\n"..goodthings[ran[1]]..
    "\r\n"..goodthings[ran[2]]..
    "\r\n"..goodthings[ran[3]]..
    "\r\n"..goodthings[ran[4]]..
    "\r\n"..
    "⛑️忌：\r\n"..badthings[ran[5]]..
    "\r\n"..badthings[ran[6]]..
    "\r\n"..
    "📆日期："..os.date("%Y年%m月%d日",time).."\r\n"..
    "🔋综合幸运指数："..tostring(math.random(0,100)).."%\r\n"..
    "🔖当日吉言："..saysTemp)
end

local function almanac(data)
    if data.msg == "今日运势" then
        return getAlmanac(os.time(),data.qq)
    elseif data.msg == "明日运势" then
        return getAlmanac(os.time()+3600*24,data.qq)
    elseif data.msg == "昨日运势" then
        return getAlmanac(os.time()-3600*24,data.qq)
    end
end


return {--今日运势
